package package_B;



public class Process {
	
	// フィールド変数の宣言
	public String country;
	public String food;
	public String foodCategory;
	
	
	
	public Process(String country, String food, String foodCategory) {
		
		// 変数に引数を代入するコンストラクタの作成
		this.country = country;
		this.food = food;
		this.foodCategory = foodCategory;
		
		
	}
}
